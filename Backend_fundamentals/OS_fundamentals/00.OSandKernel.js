// Operating System (OS):
// The operating system is the main software that acts as a bridge between hardware and applications. It manages hardware resources (CPU, memory, storage, devices) and makes sure programs run smoothly, securely, and efficiently.
// A few examples are - Windows OS, macOS and LinuxOS.

// Kernel:
// The kernel is the core part of the OS that directly talks to the hardware and controls things like process execution, memory, and device management.
// A few examples are - Linux kernel (used in Ubuntu, Fedora, Android, etc.), BSD kernels (FreeBSD, OpenBSD, NetBSD) and Older UNIX kernels
